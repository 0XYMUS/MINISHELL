#include "minishell.h"

int	main(int argc, char **argv, char **envp)
{
	t_shell	sh;

	(void)argc;
	(void)argv;
	if (shell_init(&sh, envp) != 0)
		return (1);
	shell_loop(&sh);
	return (0);
}
